package org.by.issoft.paramCollector;

public class MyClass {

	private int a;
	private String string;

	public MyClass(int a, String string) {
		this.a = a;
		this.string = string;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}
}
